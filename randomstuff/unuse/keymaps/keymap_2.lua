-- maps symbol names to symbols
local keyboard = {}

keyboard.apostrophe      = "'"
keyboard.backslash       = "\\"
keyboard.comma           = ","
keyboard.equals          = "="
keyboard.lbracket        = "["
keyboard.minus           = "-"
keyboard.period          = "."
keyboard.rbracket        = "]"
keyboard.semicolon       = ";"
keyboard.slash           = "/" -- / on main keyboard
keyboard.space           = " "

return keyboard
